# Python String isdecimal() function returns true if all characters in a string are decimal,
# else it returns False.

print("100".isdecimal())



s = "12345"
print(s.isdecimal())
  
# contains alphabets
s = "12geeks34"
print(s.isdecimal())
  
# contains numbers and spaces
s = "12 34"
print(s.isdecimal())